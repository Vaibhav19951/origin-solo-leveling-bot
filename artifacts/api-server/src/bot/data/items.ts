export function getShopItems(hunterRank: string): ShopItem[] {
  const rankOrder = ["E", "D", "C", "B", "A", "S", "NLH", "Monarch"];
  const hunterIdx = rankOrder.indexOf(hunterRank === "NLH" ? "NLH" : hunterRank);
  return SHOP_ITEMS.filter((item) => {
    const minIdx = rankOrder.indexOf(item.minRank);
    return minIdx <= hunterIdx;
  });
}
